// operator: it is a special symbol that is used to perform an operation on a variable or value.


// Arithmatic Operators: +, -, *, /, %, ++, --, = 

var var1 = 10; // number
var var2 = 20; // number
var var3 = 25;
var var6 = 10.20 // number


// addition operator: + 
console.log(var1 + var2)
console.log(var1 + var6)
console.log(var2 + var6)

// subtraction operator: -
console.log(var1 - var2);
console.log(var2 - var1);

// multiplication operator: *
console.log(var1 * var2)
console.log(var1 * var6)
console.log(var2 * var3)
console.log(var3 * var6)


// division operator: /
console.log(var1 / var2)
console.log(var1 / var6)
console.log(var3 / var1)
console.log(var6 / var2)
console.log(var2 / var6)
console.log(var2 / var1)


// modulus operator: % --> it gives the remainder of the division
var num1 = 5;
var num2 = 2;
var num3 = 3;
var num4 = 4;
/**
 * 
 * 5 / 2 = 2 
 * remainder = 1
*/
console.log(num1 % num2) // 1
console.log(num3 % num4)


